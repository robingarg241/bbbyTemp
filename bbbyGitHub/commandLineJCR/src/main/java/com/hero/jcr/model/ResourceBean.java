package com.hero.jcr.model;

public class ResourceBean {
	
	private String resourceType;
	private String damResource;
	private String tileNumber;
	private String contentResource;
	private String feedURL;
	private String feedComponent;
	private String feedColor;
	private String feedType;
	
	public String getResourceType() {
		return resourceType;
	}
	public void setResourceType(String resourceType) {
		this.resourceType = resourceType;
	}
	public String getDamResource() {
		return damResource;
	}
	public void setDamResource(String damResource) {
		this.damResource = damResource;
	}
	public String getTileNumber() {
		return tileNumber;
	}
	public void setTileNumber(String tileNumber) {
		this.tileNumber = tileNumber;
	}
	public String getContentResource() {
		return contentResource;
	}
	public void setContentResource(String contentResource) {
		this.contentResource = contentResource;
	}
	public String getFeedURL() {
		return feedURL;
	}
	public void setFeedURL(String feedURL) {
		this.feedURL = feedURL;
	}
	public String getFeedComponent() {
		return feedComponent;
	}
	public void setFeedComponent(String feedComponent) {
		this.feedComponent = feedComponent;
	}
	public String getFeedColor() {
		return feedColor;
	}
	public void setFeedColor(String feedColor) {
		this.feedColor = feedColor;
	}
	public String getFeedType() {
		return feedType;
	}
	public void setFeedType(String feedType) {
		this.feedType = feedType;
	}
	
	
	

}
